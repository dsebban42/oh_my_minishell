#include <unistd.h>
#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
		write(1, "-2147483648", 11);
	if (n < 0 && n > -2147483648)
	{
		write(1, "-", 1);
		n = n * -1;
	}
	if (n >= 10 && n <= 2147483647)
		ft_putnbr(n / 10);
	if (n > -2147483648 && n <= 2147483647)
		ft_putchar((n % 10) + 48);
}
