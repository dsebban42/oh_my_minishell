#include "libft.h"

void	print32_bits(int i)
{
	int cpt;

	cpt = -1;
	while (++cpt < 32)
	{
		if (i >> cpt & 1)
			ft_putchar('1');
		else
			ft_putchar('0');
	}
	ft_putchar('\n');
}
