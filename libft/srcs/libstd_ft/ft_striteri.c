#include <string.h>

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int i;

	i = 0;
	if (s != NULL)
		while (s[i])
		{
			if (f != NULL)
				f(i, &s[i]);
			i++;
		}
}
